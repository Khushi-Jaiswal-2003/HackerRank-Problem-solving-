a = input().split(":")
if a[2][2:4]=="AM":
    if a[0]=="12":
        a[0]="00"
    print(f'{a[0]}:{a[1]}:{a[2][0:2]}')
if a[2][2:4]=="PM":
    if a[0]=="12":
        a[0]=int(a[0])
        print(f'{a[0]}:{a[1]}:{a[2][0:2]}')
    elif int(a[0])<12:
        a[0]=int(a[0])+12
        print(f'{a[0]}:{a[1]}:{a[2][0:2]}')

